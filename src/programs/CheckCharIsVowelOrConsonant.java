package programs;

public class CheckCharIsVowelOrConsonant {

	public static void main(String[] args) {
		
		char ch='k';
		
		if(ch=='a'|| ch=='A'|| ch=='e'|| ch=='E'|| ch=='i'|| ch=='I'||
				ch=='o'|| ch=='O'||ch=='u'||ch=='U')
		{
			System.out.println("vowel");
		}
		else
		{
			System.out.println("consonant");
		}
		

	}

}
